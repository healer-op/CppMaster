#include<iostream>
using namespace std;
int main(){
    int a=4, b=5;
    cout<<"Operators in C++"<<endl;
    cout<<"The Value of a + b is "<<a+b<<endl;
    cout<<"The Value of a - b is "<<a-b<<endl;
    cout<<"The Value of a * b is "<<a*b<<endl;
    cout<<"The Value of a / b is "<<a/b<<endl;
    cout<<"The Value of a % b is "<<a%b<<endl;
    cout<<"The value of a ++ is "<<a++<<endl;
    cout<<"The value of a-- is "<<a--<<endl;
    cout<<"The value of ++a is "<<++a<<endl;
    cout<<"The value of --a is "<<--a<<endl;
    
    cout<<"The value id a==b is "<<(a==b)<<endl;
    cout<<"The value id a > b is "<<(a>b)<<endl;
    cout<<"The value id a < b is "<<(a<b)<<endl;
    cout<<"The value id a <= b is "<<(a<=b)<<endl;
    cout<<"The value id a >= b is "<<(a>=b)<<endl;
    return 0;
}